    public void createArrayListShape() {

        int X_Cordinate = 40;
        int Y_Cordinate = 50;

        for (int i = 0; i < 24; i++) {

            if (i == 12) {
                X_Cordinate = 40;
                Y_Cordinate = 90;

            }

            lbls[i] = new JLabel();
            pnlMain.add(lbls[i], new AbsoluteConstraints(X_Cordinate, Y_Cordinate, 59, 40));// x,y,width,height
            lbls[i].setHorizontalAlignment(0);
            lbls[i].setVisible(false);
            lbls[i].setBorder(new LineBorder(Color.black, 1));

            X_Cordinate += 60;

        }

        lbls[0].setVisible(true);
        lbls[1].setVisible(true);
        lbls[2].setVisible(true);

    }

    public void clearLabelsTexts() {

        for (int i = 0; i < 24; i++) {
            lbls[i].setText("");
        }
    }

    public void fillLabels() {

        if (al.getSize() == 4 || al.getSize() == 7 || al.getSize() == 13) {

            for (int i = al.getSize() - 1; (i < al.getCapacity() && i < 24); i++) {
                lbls[i].setVisible(true);

            }

        }

        clearLabelsTexts();
        for (int i = 0; (i < al.getSize() && i < 24); i++) {
            lbls[i].setFont(new Font("Tahoma", 1, 14));
            lbls[i].setHorizontalAlignment(SwingConstants.CENTER);
            lbls[i].setText(al.get(i) + "");

        }

    }

