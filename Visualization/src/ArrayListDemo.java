    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed

        String value = txtValue.getText();

        int loc = -1;

        if (value.length() == 0) {
            JOptionPane.showMessageDialog(pnlMain, "Please enter the value you want to search for");
        } else {

            loc = al.firstOccurenceOf(value);

            if (loc == -1) {
                JOptionPane.showMessageDialog(pnlMain, "The value " + value + " was not found in the list");

            } else {
                JOptionPane.showMessageDialog(pnlMain, "The value " + value + " is at index " + loc + " in the list");

            }

        }


    }//GEN-LAST:event_btnSearchActionPerformed

