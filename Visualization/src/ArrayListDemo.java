    private void btnAddByIndexActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddByIndexActionPerformed

        String value = txtValue.getText();

        if (value.length() == 0) {
            JOptionPane.showMessageDialog(pnlMain, "Please enter the value which will be added");
        }

        String indexStr = txtIndex.getText();
        if (indexStr.length() == 0) {
            JOptionPane.showMessageDialog(pnlMain, "Please enter the index");
        } else {

            int index = 0;

            boolean isValidIndex = true;

            try {
                index = Integer.parseInt(indexStr);

                al.add(index, value);

            } catch (NumberFormatException | ArrayIndexOutOfBoundsException ex) {

                // isValidIndex=false;
                if (ex instanceof NumberFormatException) {
                    JOptionPane.showMessageDialog(pnlMain, "Please enter a valid index, a positive integer only");
                }

                if (ex instanceof ArrayIndexOutOfBoundsException) {
                    JOptionPane.showMessageDialog(pnlMain, "Index= " + index + ", size= " + al.getSize()
                            + ". Index cannot be bigger than the size");
                }

            }

            System.out.println(al);
            //txtArea.append(al.toString());

            fillLabels();
            lblInfo.setText("Array List is not empty  Size = " + al.getSize() + " and Capacity is " + al.getCapacity());

        }


    }//GEN-LAST:event_btnAddByIndexActionPerformed

