    private void btnPushActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPushActionPerformed

        String value = txtValue.getText();

        if (value.length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Please Enter a value to push");
        } else if (ms.getSize() >= arrayOfLabelsSize) {
            JOptionPane.showMessageDialog(rootPane, "Stack is Full!!!");

        } else {

            if (ms.getSize() != 0) {
                peekPointerLocation -= 40;
            }

           // System.out.println("Size=" + ms.getSize() + ", peekPointerLoc=" + peekPointerLocation);

            lbls[ms.getSize()].setText(value);
            lbls[ms.getSize()].setVisible(true);
            pnlMainInside.add(lblPeek, new AbsoluteConstraints(520, peekPointerLocation, 59, 20));//// x,y,width,height
            lblPeek.setVisible(true);

            ms.push(value);

            lblTopValue.setText("Peek = " + ms.getPeek());
            lblSize.setText("Size = " + ms.getSize());

        }
    }//GEN-LAST:event_btnPushActionPerformed

    private void btnPopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPopActionPerformed

        ms.pop();

        if (ms.getSize() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Stack is Empty!!");
            lbls[0].setText("");
            lbls[0].setVisible(false);
            lblTopValue.setText("Peek = null");
            lblSize.setText("Size = 0");
            lblPeek.setVisible(false);
        } else {
            lbls[ms.getSize()].setText("");
            lbls[ms.getSize()].setVisible(false);

            pnlMainInside.remove(lblPeek);

            peekPointerLocation += 40;

            pnlMainInside.add(lblPeek, new AbsoluteConstraints(420, peekPointerLocation + 1, 59, 20));
            lblPeek.setVisible(true);

            lblTopValue.setText("Peek = " + ms.getPeek());
            lblSize.setText("Size = " + ms.getSize());
        }
    }//GEN-LAST:event_btnPopActionPerformed

    private void txtValueKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtValueKeyTyped
        char c = evt.getKeyChar();

        if (cmboDataType.getSelectedIndex() == 0) {// integer

            if (!(Character.isDigit(c) || (c == KeyEvent.VK_BACK_SPACE)
                    || (c == KeyEvent.VK_DELETE))) {

                getToolkit().beep();
                evt.consume();
                JOptionPane.showMessageDialog(rootPane, "You are allowed to enter digit only!");

            }

        } else if (cmboDataType.getSelectedIndex() == 1) {// char

            String value = txtValue.getText();

            if (value.length() > 0) {

                getToolkit().beep();
                evt.consume();
                JOptionPane.showMessageDialog(rootPane, "You are allowed to enter Characters (1 Byte) only!");
            }

        } else if (cmboDataType.getSelectedIndex() == 3) {//Double/Float

            if (c != '.' && !(Character.isDigit(c) || (c == KeyEvent.VK_BACK_SPACE)
                    || (c == KeyEvent.VK_DELETE))) {

                getToolkit().beep();
                evt.consume();
                JOptionPane.showMessageDialog(rootPane, "You are allowed to enter digits or decimal points only!");
            }

        }
    }//GEN-LAST:event_txtValueKeyTyped

