    public void createStackShape() {

        lblPeek = new JLabel("Peek-->");
        lblPeek.setFont(new Font("Thoma", 1, 14));

        pnlMainInside.add(lblPeek, new AbsoluteConstraints(420, 370, 59, 20));// x,y,width,height

        lblPeek.setVisible(false);

        for (int i = 0; i < arrayOfLabelsSize; i++) {
            lbls[i] = new JLabel();
            pnlMainInside.add(lbls[i], new AbsoluteConstraints(470, initial_Y, 59, 40));// x,y,width,height

             lbls[i].setFont(new Font("Tahoma", 1, 14));
            lbls[i].setHorizontalAlignment(SwingConstants.CENTER);
            //lbls[i].setText(i + "");
            lbls[i].setVisible(false);
            lbls[i].setBorder(new LineBorder(Color.black, 1));
            initial_Y -= 40;
        }

        //reset 
        initial_Y = 2000;
    }

    public void clearStack() {

        lblPeek.setVisible(false);
        lblTopValue.setText("");
        lblSize.setText("");
        txtValue.setText("");

        if (lbls != null) {
            for (int i = 0; i < arrayOfLabelsSize; i++) {
                lbls[i].setVisible(false);
            }
        }

        ms.clear();


    }

