    public void calculateCurrentOperation() {

        secondNum = Double.parseDouble(txtDisplay.getText());

        if (operations.equals("+")) {

            result = firstNum + secondNum;

        } else if (operations.equals("-")) {

            result = firstNum - secondNum;

        } else if (operations.equals("*")) {

            result = firstNum * secondNum;

        } else if (operations.equals("/")) {
            result = firstNum / secondNum;

        } else if (operations.equals("%")) {

            result = firstNum % secondNum;

        }

        firstNum = result;

    }

